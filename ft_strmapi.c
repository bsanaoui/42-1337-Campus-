#include "libft.h"

char	*ft_strmapi(const char *s, char (*f)(unsigned int, char))
{
	char			*p;
	unsigned int	i;

	if (!s && !f)
		return (NULL);
	p = (char *)malloc((ft_strlen(s) + 1) * sizeof(*s));
	if (!p)
		return (NULL);
	i = 0;
	while (s[i])
	{
		p[i] = f(i, s[i]);
		i++;
	}
	p[i] = '\0';
	return (p);
}
